num1 = float(input("please enter the first num :"))
num2 =float(input("please enter the second num :"))
operator = input ("please enter the operator :")
if operator == "+":
    print(num1+num2)
elif operator == "-":
    print(num1-num2)
elif operator=="*":
    print(num1*num2)
elif operator == "/":
    print(num1/num2)
else: print("wrong operator")

